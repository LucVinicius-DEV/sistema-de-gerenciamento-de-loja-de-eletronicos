package entities.enums;

/**
 * Enumeração que define as categorias de produtos eletrônicos disponíveis na loja.
 * Utilizar enum garante tipo seguro e evita erros de digitação ou valores inválidos.
 */
public enum ProductCategory {

    SMARTPHONE,  // Telefones celulares inteligentes
    LAPTOP,      // Notebooks e laptops
    TV,          // Televisores
    ACCESSORY,   // Acessórios como fones, cabos, carregadores
    GAMING,      // Produtos para jogos (consoles, controles)
    OTHER        // Outros produtos que não se encaixam nas categorias acima
}
